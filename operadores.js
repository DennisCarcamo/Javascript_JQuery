'use strict'

var numero =1;
var numero2 =5;

//suma
var resultSuma = numero + numero2; 
//resta
var resultResta =  numero - numero2; 
//multiplicacion
var resultMultiplicacion  = numero * numero2; 
//division
var resultDivision = numero / numero2; 


console.log('Resultado de la suma: ' + resultSuma);
console.log('Resultado de la resta: ' + resultResta);
console.log('Resultado de la division: '+ resultDivision);
console.log('Resultado de la multiplicacion: '+ resultMultiplicacion);

//operadores de incremento
resultSuma ++;
console.log('Despues de Incrementar: '+ resultSuma);
//operador de decrtemento
resultSuma --;
console.log('Despues de decrementar: '+ resultSuma);